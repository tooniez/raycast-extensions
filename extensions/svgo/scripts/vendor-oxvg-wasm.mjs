#!/usr/bin/env node
/**
 * Vendors @oxvg/wasm into the extension so that:
 *
 * 1. The WASM JS wrapper is converted from ESM → CJS so esbuild can bundle
 *    it statically without runtime dynamic-import hacks.
 * 2. The WASM binary is copied to assets/ so Raycast packages it with the
 *    extension (assets/ is always bundled; node_modules/ is not).
 * 3. A SHA-256 integrity constant is generated so the extension can verify
 *    the WASM binary has not been tampered with at runtime.
 *
 * Run automatically via the prebuild / predev npm lifecycle hooks.
 */

import { execFileSync } from "node:child_process";
import { copyFileSync, mkdirSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import { createHash } from "node:crypto";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

const wasmPkgDir = path.join(root, "node_modules/@oxvg/wasm/dist");
const vendorDir = path.join(root, "src/vendor");
const assetsDir = path.join(root, "assets");

// ── 1. Create output directories ─────────────────────────────────────────────

if (!existsSync(vendorDir)) mkdirSync(vendorDir, { recursive: true });

// ── 2. Convert ESM wrapper → CJS so esbuild can statically bundle it ─────────
//
// The @oxvg/wasm package only ships an ESM file. A runtime dynamic import()
// in a CJS bundle causes esbuild to emit `createRequire(import.meta.url)`
// which is undefined in a CJS context. Converting to CJS avoids this entirely.

const esbuild = path.join(root, "node_modules/.bin/esbuild");
const wasmJsSrc = path.join(wasmPkgDir, "oxvg_wasm.js");
const wasmJsDst = path.join(vendorDir, "oxvg-wasm.cjs");

execFileSync(esbuild, [
  wasmJsSrc,
  "--format=cjs",
  "--platform=node",
  `--outfile=${wasmJsDst}`,
]);

// ── 3. Copy the TypeScript declarations ──────────────────────────────────────

copyFileSync(path.join(wasmPkgDir, "oxvg_wasm.d.ts"), path.join(vendorDir, "oxvg-wasm.d.ts"));

// ── 4. Copy WASM binary to assets/ ───────────────────────────────────────────

const wasmBinSrc = path.join(wasmPkgDir, "oxvg_wasm_bg.wasm");
const wasmBinDst = path.join(assetsDir, "oxvg_wasm_bg.wasm");
copyFileSync(wasmBinSrc, wasmBinDst);

// ── 5. Generate SHA-256 integrity constant ───────────────────────────────────

const wasmBytes = readFileSync(wasmBinSrc);
const sha256 = createHash("sha256").update(wasmBytes).digest("hex");

writeFileSync(
  path.join(vendorDir, "oxvg-wasm-integrity.ts"),
  [
    "// Auto-generated by scripts/vendor-oxvg-wasm.mjs — do not edit manually.",
    `export const OXVG_WASM_SHA256 = "${sha256}";`,
    "",
  ].join("\n")
);

// ── 6. Report ─────────────────────────────────────────────────────────────────

const { version } = JSON.parse(
  readFileSync(path.join(root, "node_modules/@oxvg/wasm/package.json"), "utf8")
);
console.log(`✓ Vendored @oxvg/wasm@${version}`);
console.log(`  CJS wrapper → ${path.relative(root, wasmJsDst)}`);
console.log(`  WASM binary → ${path.relative(root, wasmBinDst)}`);
console.log(`  SHA-256     → ${sha256}`);

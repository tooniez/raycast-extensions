import { useEffect, useState } from "react";
import { Action, ActionPanel, Form, Icon, List, Toast, popToRoot, showToast, useNavigation } from "@raycast/api";
import {
  AppleTVDevice,
  PairingSession,
  finishCompanionPairing,
  serializeCredentials,
  startCompanionPairing,
} from "@bharper/atv-js";
import { deviceFromManualEntry, saveSelectedDevice, scanForDevices } from "./lib/devices";
import { saveCredentials } from "./lib/credentials";

/**
 * Pairing flow: scan → pick a device → 4-digit PIN appears on the TV →
 * enter it → credentials stored. Pairing is interactive by nature (the PIN
 * is generated by the Apple TV), so it can't be a preference.
 */
export default function Setup() {
  const [devices, setDevices] = useState<AppleTVDevice[]>([]);
  const [isLoading, setIsLoading] = useState(true);

  useEffect(() => {
    let cancelled = false;
    (async () => {
      try {
        const found = await scanForDevices(5000);
        if (!cancelled) setDevices(found);
      } catch (error) {
        await showToast({
          style: Toast.Style.Failure,
          title: "Scan Failed",
          message: error instanceof Error ? error.message : String(error),
        });
      } finally {
        if (!cancelled) setIsLoading(false);
      }
    })();
    return () => {
      cancelled = true;
    };
  }, []);

  return (
    <List isLoading={isLoading} searchBarPlaceholder="Search Apple TVs…">
      <List.EmptyView
        icon={Icon.Monitor}
        title={isLoading ? "Scanning your network…" : "No Apple TVs Found"}
        description={
          isLoading
            ? "Looking for Apple TVs via Bonjour."
            : "Make sure your Apple TV is awake and on the same network, or add it by IP address."
        }
        actions={
          <ActionPanel>
            <Action.Push title="Add by IP Address" icon={Icon.Plus} target={<ManualEntryForm />} />
          </ActionPanel>
        }
      />
      <List.Section title="Apple TVs on Your Network">
        {devices.map((device) => (
          <List.Item
            key={device.identifier}
            icon={Icon.Monitor}
            title={device.name}
            subtitle={device.address}
            accessories={[{ text: device.model }]}
            actions={
              <ActionPanel>
                <Action.Push title="Pair" icon={Icon.Link} target={<PairForm device={device} />} />
                <Action.Push title="Add Another by IP Address" icon={Icon.Plus} target={<ManualEntryForm />} />
              </ActionPanel>
            }
          />
        ))}
      </List.Section>
    </List>
  );
}

function ManualEntryForm() {
  const { push } = useNavigation();

  return (
    <Form
      navigationTitle="Add Apple TV by IP"
      actions={
        <ActionPanel>
          <Action.SubmitForm
            title="Continue to Pairing"
            icon={Icon.Link}
            onSubmit={(values: { name: string; address: string; port: string }) => {
              if (!values.address.trim()) {
                showToast({ style: Toast.Style.Failure, title: "IP address is required" });
                return;
              }
              const port = Number(values.port.trim() || "49152");
              if (!Number.isInteger(port) || port < 1 || port > 65535) {
                showToast({ style: Toast.Style.Failure, title: "Port must be a number between 1 and 65535" });
                return;
              }
              push(<PairForm device={deviceFromManualEntry(values.name.trim(), values.address.trim(), port)} />);
            }}
          />
        </ActionPanel>
      }
    >
      <Form.Description text="Use this if network discovery is blocked (VPNs, segmented Wi-Fi). Find the address on the Apple TV under Settings → Network." />
      <Form.TextField id="address" title="IP Address" placeholder="192.168.1.42" />
      <Form.TextField id="name" title="Name" placeholder="Living Room" />
      <Form.TextField
        id="port"
        title="Companion Port"
        placeholder="49152"
        info="Apple TVs use a port in the 49152–49155 range for the Companion protocol. If pairing fails with the default, try 49153 or 49154."
      />
    </Form>
  );
}

function PairForm({ device }: { device: AppleTVDevice }) {
  const [session, setSession] = useState<PairingSession | null>(null);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    let cancelled = false;
    (async () => {
      try {
        const s = await startCompanionPairing(device);
        if (!cancelled) setSession(s);
      } catch (e) {
        if (!cancelled) setError(e instanceof Error ? e.message : String(e));
      }
    })();
    return () => {
      cancelled = true;
    };
  }, [device]);

  async function submit(values: { pin: string }) {
    if (!session) return;
    const pin = values.pin.trim();
    if (!/^\d{4}$/.test(pin)) {
      await showToast({ style: Toast.Style.Failure, title: "Enter the 4-digit PIN shown on your TV" });
      return;
    }

    const toast = await showToast({ style: Toast.Style.Animated, title: "Pairing…" });
    try {
      const hapCredentials = await finishCompanionPairing(session, pin, "Raycast");
      const companion = serializeCredentials(hapCredentials);
      await saveCredentials(device.identifier, { companion, airplay: "" });
      await saveSelectedDevice(device);
      toast.style = Toast.Style.Success;
      toast.title = `Paired with ${device.name}`;
      toast.message = "You're ready. Try the Apple TV Remote command.";
      await popToRoot();
    } catch (e) {
      toast.style = Toast.Style.Failure;
      toast.title = "Pairing Failed";
      toast.message = e instanceof Error ? e.message : String(e);
    }
  }

  return (
    <Form
      navigationTitle={`Pair with ${device.name}`}
      isLoading={!session && !error}
      actions={
        <ActionPanel>
          <Action.SubmitForm title="Pair" icon={Icon.Link} onSubmit={submit} />
        </ActionPanel>
      }
    >
      {error ? (
        <Form.Description
          text={`Could not start pairing: ${error}\n\nMake sure the Apple TV is awake and try again.`}
        />
      ) : (
        <>
          <Form.Description text={`A 4-digit PIN should now be showing on “${device.name}”.`} />
          <Form.TextField id="pin" title="PIN" placeholder="1234" autoFocus />
        </>
      )}
    </Form>
  );
}
